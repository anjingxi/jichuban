package com.itheima.test;

/**
 * 循环1-5
 * 5-1
 */
public class Test02_02 {
    public static void main(String[] args) {
        for (int i = 1; i <= 5; i++) {
            System.out.print(i);
        }
        System.out.println();
        for (int i = 5; i > 0; i--) {
            System.out.print(i);

        }
    }
}
